# Los operadores de comparación (`==`, `!=`, `>`, `<`, `>=`, `<=`) se utilizan para comparar valores.

# Ejemplo de uso de operadores de comparación en `if`
a = 5
b = 10

if a == b:
    print("a es igual a b.")
else:
    print("a no es igual a b.")  # Este bloque se ejecuta

if a != b:
    print("a es diferente de b.")  # Este bloque se ejecuta

if a < b:
    print("a es menor que b.")  # Este bloque se ejecuta

if a <= b:
    print("a es menor o igual que b.")  # Este bloque se ejecuta