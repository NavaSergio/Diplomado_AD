# El operador `and` se utiliza para combinar dos condiciones.
# El resultado es `True` solo si ambas condiciones son `True`.

# Ejemplos de AND
a = 5
b = 10
c = 15
condicion1 = a < b
condicion2 = b < c
resultado_and = condicion1 and condicion2
print(condicion1, "y", condicion2, "son verdaderas:", resultado_and)  # Salida: True y True son verdaderas: True