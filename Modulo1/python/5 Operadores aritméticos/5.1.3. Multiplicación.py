# El operador `*` se utiliza para multiplicar dos números o repetir una cadena de texto.

# Ejemplos de multiplicación de números
a = 3
b = 7
multiplicacion = a * b
print("Multiplicación de", a, "y", b, "es:", multiplicacion)  # Salida: Multiplicación de 3 y 7 es: 21

# Ejemplos de repetición de cadenas
mensaje = "Hola "
repeticion = mensaje * 3
print("Mensaje repetido:", repeticion)  # Salida: Mensaje repetido: Hola Hola Hola