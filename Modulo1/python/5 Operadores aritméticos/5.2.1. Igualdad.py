# El operador `==` se utiliza para comparar si dos valores son iguales.

# Ejemplos de igualdad
a = 5
b = 5
igualdad = a == b
print(a, "es igual a", b, ":", igualdad)  # Salida: 5 es igual a 5: True

c = "Hola"
d = "Hola"
igualdad_cadenas = c == d
print(c, "es igual a", d, ":", igualdad_cadenas)  # Salida: Hola es igual a Hola: True