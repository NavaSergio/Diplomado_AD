# El operador `>=` se utiliza para comparar si un valor es mayor o igual que otro.
# El operador `<=` se utiliza para comparar si un valor es menor o igual que otro.

# Ejemplos de mayor o igual que
a = 10
b = 10
mayor_o_igual_que = a >= b
print(a, "es mayor o igual que", b, ":", mayor_o_igual_que)  # Salida: 10 es mayor o igual que 10: True

# Ejemplos de menor o igual que
c = 5
d = 7
menor_o_igual_que = c <= d
print(c, "es menor o igual que", d, ":", menor_o_igual_que)  # Salida: 5 es menor o igual que 7: True