# El operador `or` se utiliza para combinar dos condiciones.
# El resultado es `True` si al menos una de las condiciones es `True`.

# Ejemplos de OR
a = 5
b = 10
c = 3
condicion1 = a > b
condicion2 = b > c
resultado_or = condicion1 or condicion2
print(condicion1, "o", condicion2, "es verdadera:", resultado_or)  # Salida: False o True es verdadera: True