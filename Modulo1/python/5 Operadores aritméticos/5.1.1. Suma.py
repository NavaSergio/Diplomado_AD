# El operador `+` se utiliza para sumar dos números o concatenar dos cadenas de texto.

# Ejemplos de suma de números
a = 5
b = 3
suma = a + b
print("Suma de", a, "y", b, "es:", suma)  # Salida: Suma de 5 y 3 es: 8

# Ejemplos de concatenación de cadenas
nombre = "Juan"
apellido = "Pérez"
nombre_completo = nombre + " " + apellido
print("Nombre completo:", nombre_completo)  # Salida: Nombre completo: Juan Pérez