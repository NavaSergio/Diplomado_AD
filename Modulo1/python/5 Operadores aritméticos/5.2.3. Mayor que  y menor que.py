
# El operador `>` se utiliza para comparar si un valor es mayor que otro.
# El operador `<` se utiliza para comparar si un valor es menor que otro.

# Ejemplos de mayor que
a = 10
b = 5
mayor_que = a > b
print(a, "es mayor que", b, ":", mayor_que)  # Salida: 10 es mayor que 5: True

# Ejemplos de menor que
c = 3
d = 7
menor_que = c < d
print(c, "es menor que", d, ":", menor_que)  # Salida: 3 es menor que 7: True