# Los paréntesis se utilizan para cambiar el orden de evaluación de las operaciones.

# Ejemplo con paréntesis
resultado = 3 + (5 * 2)  # Primero se evalúa la multiplicación, luego la suma
print("Resultado de 3 + (5 * 2):", resultado)  # Salida: Resultado de 3 + (5 * 2): 13

# Ejemplo con paréntesis cambiando el orden
resultado_con_parentesis = (3 + 5) * 2  # Primero se evalúa la suma, luego la multiplicación
print("Resultado de (3 + 5) * 2:", resultado_con_parentesis)  # Salida: Resultado de (3 + 5) * 2: 16