# El operador `!=` se utiliza para comparar si dos valores son diferentes.

# Ejemplos de desigualdad
a = 5
b = 3
desigualdad = a != b
print(a, "es diferente de", b, ":", desigualdad)  # Salida: 5 es diferente de 3: True

c = "Hola"
d = "Adiós"
desigualdad_cadenas = c != d
print(c, "es diferente de", d, ":", desigualdad_cadenas)  # Salida: Hola es diferente de Adiós: True