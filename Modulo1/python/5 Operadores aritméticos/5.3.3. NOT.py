# El operador `not` se utiliza para negar una condición.
# Si la condición es `True`, el resultado es `False`, y viceversa.

# Ejemplos de NOT
a = 5
b = 10
condicion = a < b
resultado_not = not condicion
print("No es cierto que", a, "es mayor que", b, ":", resultado_not)  # Salida: No es cierto que 5 es mayor que 10: False