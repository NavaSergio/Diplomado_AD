# La sintaxis básica del bucle `while` es:
# while condición:
#     bloque de código

# Ejemplo de sintaxis básica del bucle `while`
contador = 0

while contador < 3:
    print("Contador:", contador)  # Este bloque se ejecuta mientras contador sea menor que 3
    contador += 1  # Incrementa el contador en 1 en cada iteración