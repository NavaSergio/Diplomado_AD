# La función `range()` genera una secuencia de números que se puede iterar.

# Ejemplo de iteración sobre un rango
for i in range(5):
    print(i)  # Este bloque se ejecuta para cada número en el rango (0, 1, 2, 3, 4)

# Ejemplo de iteración sobre un rango con inicio y fin
for i in range(2, 7):
    print(i)  # Este bloque se ejecuta para cada número en el rango (2, 3, 4, 5, 6)

# Ejemplo de iteración sobre un rango con inicio, fin y paso
for i in range(1, 10, 2):
    print(i)  # Este bloque se ejecuta para cada número en el rango (1, 3, 5, 7, 9)