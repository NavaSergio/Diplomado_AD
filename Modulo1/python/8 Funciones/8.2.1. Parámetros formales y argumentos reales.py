# Los parámetros formales son las variables que se definen en la declaración de la función.
# Los argumentos reales son los valores que se pasan a la función cuando se llama.

# Ejemplo de parámetros formales y argumentos reales
def saludar(nombre):  # `nombre` es un parámetro formal
    print("Hola,", nombre)

# Llamada a la función con un argumento real
saludar("Ana")  # "Ana" es un argumento real