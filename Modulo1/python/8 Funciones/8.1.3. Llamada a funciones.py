# Para llamar a una función, se escribe el nombre de la función seguido de paréntesis.
# Si la función tiene parámetros, se deben proporcionar los argumentos correspondientes.

# Ejemplo de llamada a funciones
def sumar(a, b):
    resultado = a + b
    print("La suma de", a, "y", b, "es:", resultado)

# Llamada a la función con dos argumentos
sumar(3, 5)  # Salida: La suma de 3 y 5 es: 8