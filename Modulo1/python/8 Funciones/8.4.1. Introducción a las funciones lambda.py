# Las funciones lambda son funciones anónimas que se definen en una sola línea.
# Se utilizan para tareas simples y breves.

# Ejemplo de función lambda
sumar = lambda a, b: a + b  # Define una función lambda que suma dos números

# Llamada a la función lambda
resultado = sumar(3, 5)
print("La suma es:", resultado)  # Salida: La suma es: 8