# La sintaxis básica para definir una función es:
# def nombre_de_la_funcion(parametros):
#     bloque de código

# Ejemplo de sintaxis básica de definición de funciones
def saludar(nombre):
    print("Hola,", nombre)  # Este bloque de código se ejecuta cuando se llama a la función

# Llamada a la función con un argumento
saludar("Juan")  # Salida: Hola, Juan