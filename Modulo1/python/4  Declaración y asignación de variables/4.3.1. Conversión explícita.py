# La conversión explícita se realiza utilizando funciones de conversión como `int()`, `float()`, `str()`, `bool()`.

# Ejemplos de conversión explícita
numero_str = "123"
numero_int = int(numero_str)  # Convertir de cadena a entero
numero_float = float(numero_str)  # Convertir de cadena a flotante

# Imprimir los valores convertidos
print(numero_int)  # Salida: 123
print(numero_float)  # Salida: 123.0

# Convertir de entero a cadena
edad = 25
edad_str = str(edad)

# Imprimir el valor convertido
print(edad_str)  # Salida: "25"

# Convertir de flotante a entero
altura = 1.75
altura_int = int(altura)

# Imprimir el valor convertido
print(altura_int)  # Salida: 1

# Convertir de booleano a cadena
es_estudiante = True
es_estudiante_str = str(es_estudiante)

# Imprimir el valor convertido
print(es_estudiante_str)  # Salida: "True"