# Una variable es un espacio en la memoria donde se puede almacenar un valor.
# En Python, las variables no necesitan ser declaradas con un tipo de dato específico.
# El tipo de dato se determina automáticamente cuando se asigna un valor a la variable.

# Ejemplo de declaración y asignación de una variable
nombre = "Juan"  # 'nombre' es una variable que almacena una cadena de texto
edad = 25        # 'edad' es una variable que almacena un número entero
altura = 1.75    # 'altura' es una variable que almacena un número flotante
es_estudiante = True  # 'es_estudiante' es una variable que almacena un valor booleano

# Imprimir los valores de las variables
print(nombre)      # Salida: Juan
print(edad)        # Salida: 25
print(altura)      # Salida: 1.75
print(es_estudiante)  # Salida: True