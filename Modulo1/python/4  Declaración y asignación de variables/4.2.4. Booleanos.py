# Los booleanos son valores que pueden ser `True` (verdadero) o `False` (falso).

# Ejemplos de booleanos
es_estudiante = True
es_mayor_de_edad = False
esta_lloviendo = True

# Imprimir los valores de las variables
print(es_estudiante)  # Salida: True
print(es_mayor_de_edad)  # Salida: False
print(esta_lloviendo)  # Salida: True