# La conversión implícita ocurre automáticamente cuando se realizan operaciones entre diferentes tipos de datos.

# Ejemplos de conversión implícita
numero_int = 10
numero_float = 5.5

# Suma de un entero y un flotante
resultado = numero_int + numero_float

# Imprimir el resultado
print(resultado)  # Salida: 15.5 (el entero se convierte a flotante)

# Concatenación de una cadena y un entero
nombre = "Juan"
edad = 25

# Concatenar una cadena y un entero
mensaje = nombre + " tiene " + str(edad) + " años."

# Imprimir el mensaje
print(mensaje)  # Salida: Juan tiene 25 años.