# Las variables en Python deben seguir ciertas reglas de nomenclatura:
# 1. Deben comenzar con una letra o un guión bajo (_).
# 2. Pueden contener letras, números y guiones bajos.
# 3. No pueden contener espacios ni caracteres especiales.
# 4. Deben ser sensibles a mayúsculas y minúsculas (case-sensitive).

# Ejemplos de nombres de variables válidos
nombre_completo = "María López"
edad_actual = 28
_altura = 1.70
esEstudiante = True

# Ejemplos de nombres de variables no válidos
# 3edad = 30  # No puede comenzar con un número
# nombre completo = "Juan Pérez"  # No puede contener espacios
# es_estudiante! = False  # No puede contener caracteres especiales

# Imprimir los valores de las variables válidas
print(nombre_completo)  # Salida: María López
print(edad_actual)      # Salida: 28
print(_altura)          # Salida: 1.70
print(esEstudiante)     # Salida: True