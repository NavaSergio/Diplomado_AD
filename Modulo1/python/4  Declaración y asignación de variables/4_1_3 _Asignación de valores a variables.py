# En Python, se puede asignar un valor a una variable en cualquier momento.
# El valor asignado puede ser de cualquier tipo de dato.

# Ejemplo de asignación de valores a variables
nombre = "Pedro"  # Asignación de una cadena de texto
edad = 22         # Asignación de un número entero
altura = 1.80     # Asignación de un número flotante
es_estudiante = True  # Asignación de un valor booleano

# Imprimir los valores de las variables
print(nombre)      # Salida: Pedro
print(edad)        # Salida: 22
print(altura)      # Salida: 1.80
print(es_estudiante)  # Salida: True