# En Python, la declaración de una variable se realiza simplemente asignando un valor a un nombre.
# No es necesario especificar el tipo de dato.

# Ejemplo de declaración de variables
nombre = "Ana"  # 'nombre' es una variable de tipo cadena (str)
edad = 30       # 'edad' es una variable de tipo entero (int)
altura = 1.65   # 'altura' es una variable de tipo flotante (float)
es_estudiante = False  # 'es_estudiante' es una variable de tipo booleano (bool)

# Imprimir los valores de las variables
print(nombre)      # Salida: Ana
print(edad)        # Salida: 30
print(altura)      # Salida: 1.65
print(es_estudiante)  # Salida: False